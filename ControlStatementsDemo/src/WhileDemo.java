public class WhileDemo 
{

	public static void main(String[] args) 
	{
		
		//loop control variable
		int i = 0;
		int j = 0;
		
		while(i < 10 || j < 5)
		{
			System.out.println("i = " + i);
			System.out.println("j = " + j);
			
			i++;
			j++;
		}
		
		System.out.println("After while loop");
		
	}

}
